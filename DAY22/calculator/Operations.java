package calculator;

public class Operations {

	public int sum(int num1 , int num2)
	{
		int result;
		result = num1 + num2;
		return result ;
	}
	
	public double multiply(double num1 , double num2)
	{
		double result;
		result = num1 * num2;
		return result ;
	}
	
	public double division(double num1 , double num2)
	{
		double result;
		result = num1 / num2;
		return result ;
	}

}
